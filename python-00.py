'''
print()
--------
Esta función permite mostrar en pantalla un número, 
una operación, un texto, una variable o una estructura.
'''

print(6)                            # 6
print(6.5)                          # 6.5
print(3 * 5)                        # 15
print('¡Hola!')                     # ¡Hola!
print(2 > 4)                        # False

'''
Para imprimir con formato, añadimos una f después del paréntesis
y entre corchetes {} incluimos el elemento "variable".
Más adelante veremos su utilidad real.
'''
print(f'{3} x {5} = {3 * 5}')       # 3 x 5 = 15
print(f'Vamos a pagar {3 + 6} €')   # Vamos a pagar 9 €