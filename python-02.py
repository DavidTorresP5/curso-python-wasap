'''
Variables
---------
Es una forma de almacenar información en la memoria del PC asignándole
un nombre: 

nombre_variable = 'información'

Podemos usar casi cualquier palabra para denominar a una variable, 
siempre que empiece por una letra y no contenga espacios.
Es recomendable que el nombre tenga relación con el dato que almacena.
'''

nombre = 'David'
edad = 30
print(f'{nombre} tiene {edad} años')
# David tiene 30 años

num1 = 12
num2 = 3
print(num1 - num2)
# 9

pais, habitantes = 'Países Bajos', 17_181_084
print(f'El número de habitantes de {pais} es {habitantes}.')
# El número de habitantes de Países Bajos es 17181084.